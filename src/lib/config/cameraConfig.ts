// ==================== 缩放范围控制 ====================

/**
 * 🔧 缩放距离限制
 * 控制相机与目标点之间的最小和最大允许距离（单位：AU）
 */
export const ZOOM_LIMITS = {
  /** 最小缩放距离（AU）
   * 当前值：1e-10 AU
   * 换算说明：1 AU = 1.496×10¹¹ m，因此 1e-10 AU = 1.496×10¹ m ≈ 15m
   * 允许相机贴近地球表面查看车辆级别细节（约 15m 分辨率）。
   * 注：原值 0.00001 AU = 1496 km，过大；1e-10 AU ≈ 15m 更合理。
   */
  minDistance: 1e-10,

  /** 最大缩放距离（AU）
   * 当前值：1e16 AU
   * 换算说明：可观测宇宙半径约 465 亿光年 = 4.65×10¹⁰ × 63241 AU ≈ 2.94×10¹⁵ AU
   * 设置为 1e16 AU 允许缩放到可观测宇宙半径的约 3.4 倍，
   * 确保用户可以在宇宙尺度下自由探索。
   */
  maxDistance: 1e16,

  /** 缩放速度因子（无量纲，OrbitControls 使用）
   * 控制 OrbitControls 内置缩放的速度倍率，建议范围：0.5~3.0
   */
  zoomSpeed: 1.5,
};

// ==================== 防穿透设置 ====================

/**
 * 🔧 防穿透约束设置
 * 防止相机穿入天体内部，确保相机始终保持在天体表面之外
 */
export const PENETRATION_PREVENTION = {
  /** 安全距离倍数（相对于天体半径，无量纲）
   * 当前值：1.000001
   * 计算说明：地球半径 ≈ 6371 km = 6.371×10⁶ m
   *   安全距离 = 半径 × 1.000001 = 半径 + 半径 × 0.000001
   *   额外距离 = 6.371×10⁶ m × 0.000001 ≈ 6.37m ≈ 6m
   * 即相机最近可贴近到地表上方约 6m 处，允许查看车辆级别细节。
   * 注：原值 1.001 对应地表上方约 6433m（6.4km），过于保守。
   */
  safetyDistanceMultiplier: 1.000001,

  /** 约束平滑度（无量纲，范围 0~1）
   * 当相机触碰安全边界时，向安全位置推回的插值速度。
   * 值越大推回越快（但可能有突变感），值越小推回越平滑（但可能短暂穿入）。
   */
  constraintSmoothness: 0.15,

  /** 是否强制立即修正（布尔值）
   * true：检测到穿透时立即跳到安全位置（无过渡动画）
   * false：使用 constraintSmoothness 平滑推回
   */
  forceSnap: false,

  /** 调试模式（布尔值）
   * true：在控制台输出防穿透约束的触发信息，用于调试
   * false：静默运行
   */
  debugMode: false,
};

// ==================== 视角控制 ====================

/**
 * 🔧 视角与渲染平面设置
 * 控制相机的视野角度和近/远裁切平面
 */
export const VIEW_SETTINGS = {
  /** 视野角度（FOV，度，范围 1~179）
   * 垂直方向的视野角度，值越大视野越宽（鱼眼效果），值越小视野越窄（望远镜效果）。
   * 45° 是接近人眼自然视角的常用值。
   */
  fov: 45,

  /** 动态近平面调整倍数（无量纲）
   * 当前值：0.01
   * 作用：近平面距离 = 相机到目标距离 × dynamicNearPlaneMultiplier
   * 动态近平面根据相机距离自动调整，避免两个极端问题：
   *   - 近平面过近：导致深度缓冲精度不足（Z-fighting，远处物体闪烁）
   *   - 近平面过远：导致近处物体被裁切（穿模）
   * 0.01 表示近平面始终保持在相机距离的 1%，在宇宙尺度下保持良好的深度精度。
   */
  dynamicNearPlaneMultiplier: 0.01,

  /** 最小近平面距离（AU）
   * 当相机极近时（如 1e-10 AU），动态计算值可能过小，
   * 此值作为下限防止近平面距离趋近于零导致渲染异常。
   */
  minNearPlane: 0.000001,

  /** 最大远平面距离（AU）
   * 1e12 AU ≈ 1.58×10⁸ 光年，足以覆盖可观测宇宙的大部分范围。
   * 远平面过大会降低深度缓冲精度，但配合对数深度缓冲可缓解此问题。
   */
  maxFarPlane: 1e12,
};

